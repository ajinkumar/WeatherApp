    // Converts to fahrenheit
    private void convertCelsiusToFahrenheit(float celsius) {
        SharedPreferences sp = PreferenceManager.getDefaultSharedPreferences(MainActivity.this);

        temperature =  ((celsius * 9) / 5) + 32;
        todayTemperature.setText(new DecimalFormat("0.#").format(temperature) + " " + sp.getString("unit", "°F"));

    }

    // Converts to celcius
    private void convertFahrenheitToCelsius(float fahrenheit) {
        SharedPreferences sp = PreferenceManager.getDefaultSharedPreferences(MainActivity.this);

        temperature =  ((fahrenheit - 32) * 5 / 9);
        todayTemperature.setText(new DecimalFormat("0.#").format(temperature) + " " + sp.getString("unit", "°C"));

    }

